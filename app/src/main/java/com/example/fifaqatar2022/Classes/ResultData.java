package com.example.fifaqatar2022.Classes;

public class ResultData {

    String homeTeamName;
    String visitorTeamName;

    String homeTeamScore;
    String visitorTeamScore;

    public String getHomeTeamName() {
        return homeTeamName;
    }

    public void setHomeTeamName(String homeTeamName) {
        this.homeTeamName = homeTeamName;
    }

    public String getVisitorTeamName() {
        return visitorTeamName;
    }

    public void setVisitorTeamName(String visitorTeamName) {
        this.visitorTeamName = visitorTeamName;
    }

    public String getHomeTeamScore() {
        return homeTeamScore;
    }

    public void setHomeTeamScore(String homeTeamScore) {
        this.homeTeamScore = homeTeamScore;
    }

    public String getVisitorTeamScore() {
        return visitorTeamScore;
    }

    public void setVisitorTeamScore(String visitorTeamScore) {
        this.visitorTeamScore = visitorTeamScore;
    }
}
